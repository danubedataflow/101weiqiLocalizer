/*
    Recurse through DOM elements.  If element is a text element process it, otherwise recurse.
*/
function recursiveReplace(node) {
    if (node.nodeType == 3 && node.nodeName != 'SCRIPT') {
        node.nodeValue = replaceInString(node.nodeValue)
        linkSerialNumbers(node);
    } else if (node.nodeType == 1 && node.nodeName != 'SCRIPT' && node.nodeName != 'STYLE') {
        var child = node.firstChild;
        while (child) {
        	recursiveReplace(child);
        	child = child.nextSibling;
        }
    }
}

function replaceInString(s) {
    // non-fixed strings

    s = s.replaceAll(re_date, (match, year, month, day) => [ year, month, day ].join('.'))
    s = s.replaceAll(re_problem_number_1, (match, number) => `Problem ${number}`)
    s = s.replaceAll(re_problem_number_2, (match, number) => `Problem ${number}`)
    s = s.replaceAll(re_part_number, (match, number) => `Part ${number}`)
    s = s.replaceAll(re_round_number, (match, number) => `Round ${number}`)
    s = s.replaceAll(re_problems_in_total, (match, number) => `${number} problems in total`)
    s = s.replaceAll(re_min_limit, (match, limit) => `${limit} min limit`)
    s = s.replaceAll(re_times, (match, times) => `${times} times`)
    s = s.replaceAll(re_every_n_days, (match, n) => `Every ${n} days`)
    s = s.replaceAll(re_volume, (match, n) => `Volume ${n}`)
    s = s.replaceAll(re_chapter, (match, n) => `Chapter ${n}`)
    s = s.replaceAll(re_shape, (match, n) => `Shape ${n}`)
    s = s.replaceAll(re_books_in_total, (match, n) => `${n} books in total`)
    s = s.replaceAll(re_got_number, (match, n) => `Got ${n}`)
    s = s.replaceAll(re_level, (match, n) => `Level ${n}`)
    s = s.replaceAll(re_from_to, (match, n) => `From ${n} to`)
    s = s.replaceAll(re_results, (match, n) => `${n} results`)

    for (const [key, value] of Object.entries(textReplacements)) {
        // add a space because Chinese doesn't have spaces
        s = s.replaceAll(key, ' ' + value + ' ')
    }
    return s
}

function replaceAttributes() {
    document.querySelectorAll('[label]').forEach((el) => {
        el.label = replaceInString(el.label)
    })
    document.querySelectorAll('[title]').forEach((el) => {
        el.title = replaceInString(el.title)
    })
    document.querySelectorAll('input[value]').forEach((el) => {
        el.value = replaceInString(el.value)
    })
    document.querySelectorAll('input[placeholder]').forEach((el) => {
        el.placeholder = replaceInString(el.placeholder)
    })
}

function replaceImageSources(node) {
    node.querySelectorAll('img').forEach(img => {
        for (const [key, value] of Object.entries(imageReplacements)) {
            img.src = img.src.replace(key, value)
        }
    })
}

/*
 * Convert a serial number (like 'Q-12345') to a link for that problem's page.
 *
 * The serial number will be part of a text node, like 'before Q-12345 after'.
 * Parse the parts of the string, create text nodes for the 'before' and
 * 'after' parts and a link element for the serial number. Then replace the
 * original text node with those three elements.
 */

function linkSerialNumbers(textNode) {
    const match = /^(?<before>.*)Q-(?<serial>\d+)(?<after>.*)$/.exec(
        textNode.textContent);

    /*
     * Without this, linkSerialNumbers() for some reason produces an endless
     * loop. Even without the endless loop, we need to make sure that the regex
     * matched.
     */
    if (match === null) return;

    const groups = match.groups;
    const textBefore = document.createTextNode(' ' + groups.before +
        ' ');
    const textAfter = document.createTextNode(' ' + groups.after +
        ' ');

    const link = document.createElement('a');
    const linkText = document.createTextNode('Q-' + groups.serial);
    link.appendChild(linkText);
    link.setAttribute('href', 'https://www.101weiqi.com/q/' + groups.serial);

    textNode.replaceWith(textBefore, link, textAfter);
}

